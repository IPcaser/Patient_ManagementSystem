    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        if(jTextField3.getText().length() > 10 || jTextField3.getText().length() < 10){
            jLabel9.setText("InValid Number!!");
        }
        else
        {
           if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || 
            jTextField4.getText().isEmpty() || jTextField5.getText().isEmpty() || jTextField6.getText().isEmpty() || jTextField7.getText().isEmpty())
           {
               JOptionPane.showMessageDialog(null, "Please fill all Fields!!");
           }
           else
           {
                       String patientID = jTextField1.getText();
                       String name = jTextField2.getText();
                       String contactNumber = jTextField3.getText();
                       String age = jTextField4.getText();
                       String gender = (String) jComboBox1.getSelectedItem(); 
                       String bloodGroup = jTextField5.getText();
                       String address = jTextField6.getText();
                       String anyMajorIssue = jTextField7.getText();
        
        try
        {
            Connection con = Database.dbConnection();
            Statement st = con.createStatement();
            st.executeUpdate("insert into patient values('"+patientID+"','"+name+"','"+contactNumber+"','"+age+"','"+gender+"','"
                    + ""+bloodGroup+"','"+address+"','"+anyMajorIssue+"')");
            JOptionPane.showMessageDialog(null,"Successfully Inserted!!");  
            setVisible(false);
            new AddNewPatientRecord().setVisible(true);
     
        }catch(Exception e){
         //JOptionPane.showMessageDialog(null, "Please enter data in Correct Format!!");
            System.out.println(e);
        }
               
          }
        

        
        }
    }//GEN-LAST:event_jButton2ActionPerformed

